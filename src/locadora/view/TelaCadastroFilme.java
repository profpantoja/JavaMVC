    private void salvarFilme(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarFilme
        int duração = Integer.parseInt(jSpinnerDuracao.getValue().toString());
        String genero = jComboBoxGenero.getSelectedItem().toString();
        boolean sucesso;

        try {
            FilmeController filmeController = new FilmeController();
            
            if(this.codFilme == 0) {
                sucesso = filmeController.cadastrarFilme(jTextTitulo.getText(), genero, jTextAreaSinopse.getText(), duração);
            } else {
                sucesso = filmeController.alterarFilme(this.codFilme, jTextTitulo.getText(), genero, jTextAreaSinopse.getText(), duração);
            }
                        
            if (sucesso == true) {
                JOptionPane.showMessageDialog(null, "O cadastro foi realizado com sucesso!");
                this.limparTelaCadastroFilme(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Os campos não foram preenchidos corretamente.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex);
        }
    }//GEN-LAST:event_salvarFilme

