    public void buscarFilme(Integer codFilme, String titulo, String gênero, String sinopse, Integer duração) {
        this.codFilme = codFilme;
        this.jTextTitulo.setText(titulo);
        for(int contador=0;contador<jComboBoxGenero.getItemCount();contador++) {
            if (jComboBoxGenero.getItemAt(contador).equals(gênero))
                jComboBoxGenero.setSelectedIndex(contador);
        }
        this.jTextAreaSinopse.setText(sinopse);
        this.jSpinnerDuracao.setValue(duração);
    }
    
